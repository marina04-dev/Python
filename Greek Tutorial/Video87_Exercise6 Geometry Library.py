def triangle_area(base,height):
    return (base*height)/2
    
    
def square_perimeter(side):
    return 4*side
    

def square_area(side_sq):
    return side_sq**2
    
    
def circle_perimeter(r_circle):
    return 2*3.14*r_circle
    
    
def circle_area(circle_r):
    return 3.14*(circle_r**2)
    
    
    
print(square_area(8))
print(circle_area(6))
print(circle_perimeter(4.5))
print(square_perimeter(9.8))
print(triangle_area(6,7))
