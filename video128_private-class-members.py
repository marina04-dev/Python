# Class: Private members example
class Cow:
    def __init__(self,weight,hunger):
        self.weight = weight
        self.__hunger = hunger # private member
    def express(self):
        if self.__hunger>5:
            print("Mowwwwwwwwww")
        else:
            print("Mow")

molly=Cow(100,5)
print(molly.__hunger)
print(molly.weight)
molly.express()
        
